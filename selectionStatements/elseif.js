function wish(message){
    if(message=="gm"){
        console.log("good morning")
    }
    else if(message=="ga"){
        console.log("good afternoon")
    }
    else if(message=="ge"){
        console.log("good evening")
    }
    else if(message=="gn"){
        console.log("good night")
    }
    else{
        console.log("there is nothing to wish")
    }
}

wish("gn")
