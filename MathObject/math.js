//Math Object:
// Math is an inbuilt object in javascript which can be used
// to perform some specific math operation

console.log(Math.PI)            //print value of pi
console.log(Math.sqrt(49))      //print the square root of number
console.log(Math.pow(12,4))     //print the power of number
console.log(Math.round(5.6))    //round to nearest number
console.log(Math.floor(2.5))    //floor it to nearest integer value
console.log(Math.trunc(239389.48727)) //it will print integer value and remove the decimal value
console.log(Math.sign(-93819))
console.log(Math.sign(1773))    //print the number is negative or positive
console.log(Math.random())      //print the random numbers
